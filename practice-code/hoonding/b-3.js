function randMax(max) {
    return Math.trunc(1E9 * Math.random()) % max;
}

var reel = {
    symbols: [
        "♠", "♥", "♦", "♣", "☺", "★", "☾", "☀"
    ],
    spin() {
        if (this.position == null) {
            this.position = randMax(
                this.symbols.length - 1
            );
        }
        this.position = (
            this.position + 100 + randMax(100)
        ) % this.symbols.length;
    },
    display() {
        if (this.position == null) {
            this.position = randMax(
                this.symbols.length - 1
            );
        }
        return this.symbols[this.position];
    }
};

var slotMachine = {
    reels: [
        Object.create(reel),
        Object.create(reel),
        Object.create(reel)
    ],
    spin() {
        this.reels.forEach(function spinReel(reel){
            reel.spin();
        });
    },
    display() {
        this.reels.forEach(function displayThree(reel){
            let [first, second, third] = ['', '', ''];
            reel.position = reel.position - 1 < 0 ? reel.symbols.length - 1 : reel.position - 1; 
            first = reel.display()

            reel.position = (reel.position + 1) % reel.symbols.length
            second = reel.display()

            reel.position = (reel.position + 1) % reel.symbols.length
            third = reel.display()

            console.log(`${first} | ${second} | ${third}`);
        })
        console.log('\n');
    }
};

slotMachine.spin();
slotMachine.display();
// ☾ | ☀ | ★
// ☀ | ♠ | ☾
// ♠ | ♥ | ☀

